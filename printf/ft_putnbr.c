#include "ft_printf.h"

int	ft_putnbr(int n)
{
	char	c;
	static	int	i;

	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	else if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
		i++;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		c = '0' + n % 10;
		i++;
	}
	else
	{
		c = '0' + n;
		i++;
	}
	write(1, &c, 1);

	return (i);
}
